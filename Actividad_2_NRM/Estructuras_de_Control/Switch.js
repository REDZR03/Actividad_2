let q1=10

switch(q1){
    case 10:
        console.log("Buen trabajo");
        break;
    case 9:
        console.log("Falto poco");
        break;
    case 8:
        console.log("Casi perfecto");
        break;
    case 7:
        console.log("Estudia mas");
        break;
    case 6:
        console.log("Rconsidera tu carrera");
        break;
    default:
        console.log("error");
        break;
}